-- Activities [ent3]
create table [dbo].[ACTIVITIES] (
   [OID]  int  not null,
   [FIRST_NAME]  varchar(255),
   [IMAGE]  varchar(255),
   [DESCRIPTION]  varchar(255),
   [ACTIVITY_TYPE]  varchar(255),
   [DATE]  date,
   [Z_NUMBER]  varchar(255),
   [EMAIL]  varchar(255),
   [LAST_NAME]  varchar(255),
  primary key ([OID])
);


